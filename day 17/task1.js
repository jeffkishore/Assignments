var name = "kishore";     // can be re-declared and updated
let age = 21;          // can be updated but not re-declared in the same scope
const city = "Chennai";  // cannot be updated or re-declared

console.log("name:",name);
console.log("age :",age);
console.log("ciy :",city);

// var → function-scoped, can be re-declared and updated.

// let → block-scoped, can be updated but not re-declared.

// const → block-scoped, cannot be updated or re-declared (used for constants).


