let clr = "red";

switch (clr){
    case "red":
        console.log("Rose is Red");
        break;
    case "blue":
        console.log("Sky is Blue");
        break;
    case "green":
        console.log("Nature is green");
        break;
    case "black":
        console.log("Hair is black");
        break;
    default:
        console.log("invalid color");        
}
